create policy "users update own notifications"
on public.notifications for update
to authenticated
using (
  exists (
    select 1 from public.users profile
    where profile.id = notifications.recipient_id
      and profile.auth_user_id = auth.uid()
  )
)
with check (
  exists (
    select 1 from public.users profile
    where profile.id = notifications.recipient_id
      and profile.auth_user_id = auth.uid()
  )
);

