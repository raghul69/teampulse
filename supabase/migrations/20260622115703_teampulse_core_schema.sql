create policy "users read own profile"
on public.users for select
to authenticated
using (auth_user_id = auth.uid());

create policy "managers read team profiles"
on public.users for select
to authenticated
using (
  exists (
    select 1 from public.users manager_profile
    where manager_profile.auth_user_id = auth.uid()
      and manager_profile.role = 'manager'
      and users.manager_id = manager_profile.id
  )
);

create policy "admins read all profiles"
on public.users for select
to authenticated
using (
  exists (
    select 1 from public.users admin_profile
    where admin_profile.auth_user_id = auth.uid()
      and admin_profile.role = 'admin'
  )
);

create policy "departments readable by authenticated users"
on public.departments for select
to authenticated
using (true);

create policy "employees read own leave balances"
on public.leave_balances for select
to authenticated
using (
  exists (
    select 1 from public.users profile
    where profile.id = leave_balances.user_id
      and profile.auth_user_id = auth.uid()
  )
);

create policy "managers read team leave balances"
on public.leave_balances for select
to authenticated
using (
  exists (
    select 1
    from public.users employee_profile
    join public.users manager_profile on manager_profile.id = employee_profile.manager_id
    where employee_profile.id = leave_balances.user_id
      and manager_profile.auth_user_id = auth.uid()
      and manager_profile.role = 'manager'
  )
);

create policy "admins read all leave balances"
on public.leave_balances for select
to authenticated
