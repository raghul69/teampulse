alter table public.leave_types enable row level security;
alter table public.leave_policies enable row level security;
alter table public.holidays enable row level security;

drop policy if exists "leave types readable by authenticated users" on public.leave_types;
drop policy if exists "admins manage leave types" on public.leave_types;
drop policy if exists "leave policies readable by authenticated users" on public.leave_policies;
drop policy if exists "admins manage leave policies" on public.leave_policies;
drop policy if exists "holidays readable by authenticated users" on public.holidays;
drop policy if exists "admins manage holidays" on public.holidays;

create policy "leave types readable by authenticated users"
on public.leave_types for select
to authenticated
using (is_active = true or app_private.is_admin());

create policy "admins manage leave types"
on public.leave_types for all
to authenticated
using (app_private.is_admin())
with check (app_private.is_admin());

create policy "leave policies readable by authenticated users"
on public.leave_policies for select
to authenticated
using (is_active = true or app_private.is_admin());

create policy "admins manage leave policies"
on public.leave_policies for all
to authenticated
using (app_private.is_admin())
with check (app_private.is_admin());

create policy "holidays readable by authenticated users"
on public.holidays for select
to authenticated
using (
  department_id is null
  or app_private.is_admin()
  or exists (
    select 1
    from public.users profile
    where profile.auth_user_id = auth.uid()
      and profile.department_id = holidays.department_id
  )
);

create policy "admins manage holidays"
on public.holidays for all
to authenticated
using (app_private.is_admin())
with check (app_private.is_admin());
